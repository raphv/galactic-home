function makeIconSvg(iconstr) {
    return `<svg width="${5*ICON_WIDTH}" height="${5*ICON_HEIGHT}" viewBox="0 0 ${ICON_WIDTH} ${ICON_HEIGHT}" title="${iconstr}">${PIXEL_DEF}`
        + Array.from(iconstr).map(
            (c, i) => `<use href="#sc" transform="translate( ${i % ICON_WIDTH} ${Math.floor(i/ICON_WIDTH)})" r=".4" fill="${BASE_COLOURS[parseInt(c,16)]}" />` 
            ).join('')
        + '</svg>'
}

